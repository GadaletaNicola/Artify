package com.example.artify;

public class Museo {
    private String nome = "";
    private String img = "";

    public Museo(){}

    public String getImg() {
        return img;
    }

    public String getNome() {
        return nome;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
